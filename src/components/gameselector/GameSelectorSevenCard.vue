<template>
    <form class="seven-card inner hide-required" @submit.prevent="submit">
        <h4>Seven Card</h4>

        <fancy-input label="Ante" type="number" :min="25" :max="400" :step="25" v-model="ante" unit="Â¢" />

        <label class="variant">
            <span>Variant</span>
            <select v-model="variant">
                <option value="stud">Seven-Card Stud</option>
                <option value="low-card-wild">Low Card Wild</option>
                <option value="baseball">Baseball</option>
                <option value="follow-the-queen">Follow the Queen</option>
            </select>
        </label>

        <div class="buttons">
            <button>Start</button>
        </div>
    </form>
</template>

<script>
import FancyInput from "@/components/FancyInput"

export default {
    name: "GameSelectorSevenCard",
    components: {FancyInput},
    data() {
        return {
            ante: '25',
            variant: 'stud',
        }
    },
    methods: {
        submit() {
            this.$emit('submit', {
                game: 'seven-card',
                opts: {
                    ante: parseInt(this.ante, 10),
                    variant: this.variant,
                },
            })
        },
    },
}
</script>

